#!/usr/bin/env python3
"""Write slide-ready compact tables to ../../slides/artifacts/tables/.

For each model in {probit, logit}, emits an s.e.-ratio table
(s.e. ratio = sqrt(ARE)), 2 decimals, J panels x S rows x N columns.
Missing cells are rendered as '--'.
"""
from pathlib import Path
import numpy as np

SCRIPT_DIR = Path(__file__).parent
PROJ_ROOT = SCRIPT_DIR.parent.parent
OUT = PROJ_ROOT / "slides" / "artifacts" / "tables"

S_MODES = [("one", r"$S{=}1$"),
            ("sqrt_n", r"$S{=}\sqrt{N}$"),
            ("match_n", r"$S{=}N$")]
J_PANELS = [(2, r"Binary choice ($M=2$)"),
             (10, r"Multinomial ($M=10$)")]
N_VALUES = [100, 500, 1000]


def se_ratio(npz_path):
    d = dict(np.load(npz_path))
    if len(d["beta_mle"]) < 2:
        return None
    var_mle = np.var(d["beta_mle"], axis=0, ddof=1).mean()
    var_cb = np.var(d["beta_cb"], axis=0, ddof=1).mean()
    return np.sqrt(var_cb / var_mle) if var_mle > 0 else None


def cell(model, s_mode, J, N):
    p = (SCRIPT_DIR / "results" / model / "raw" / s_mode
         / f"{model}_J{J}_N{N}.npz")
    if not p.exists():
        return "--"
    v = se_ratio(p)
    return f"{v:.2f}" if v is not None else "--"


def has_any(model, s_mode):
    return any(
        (SCRIPT_DIR / "results" / model / "raw" / s_mode
         / f"{model}_J{J}_N{N}.npz").exists()
        for J, _ in J_PANELS for N in N_VALUES
    )


def build_table(model):
    # Only include S modes that have at least one cell for this model.
    active_s = [(sm, lbl) for sm, lbl in S_MODES if has_any(model, sm)]

    lines = [
        f"% Auto-generated by numerical_experiments/unit_demand/sync_to_slides.py",
        f"% model: {model}, metric: s.e. ratio = sqrt(Var_CB / Var_MLE)",
        r"\begin{tabular}{l rrr}",
        r"\toprule",
        " & " + " & ".join(f"$N={N}$" for N in N_VALUES) + r" \\",
        r"\midrule",
    ]
    for i, (J, panel_label) in enumerate(J_PANELS):
        lines.append(rf"\multicolumn{{4}}{{l}}{{\textit{{{panel_label}}}}} \\[2pt]")
        for sm, sm_label in active_s:
            row = [cell(model, sm, J, N) for N in N_VALUES]
            lines.append(f"  {sm_label} & " + " & ".join(row) + r" \\")
        if i < len(J_PANELS) - 1:
            lines[-1] += "[4pt]"
    lines += [r"\bottomrule", r"\end{tabular}"]
    return "\n".join(lines) + "\n"


def main():
    OUT.mkdir(parents=True, exist_ok=True)
    for model in ("probit", "logit"):
        target = OUT / f"tab_unit_demand_{model}.tex"
        target.write_text(build_table(model))
        print(f"wrote {target.relative_to(PROJ_ROOT)}")


if __name__ == "__main__":
    main()
