#!/usr/bin/env python3
"""Write slide-ready compact tables to ../../slides/artifacts/tables/.

For each model in {probit, logit}, emits an s.e.-ratio table
(s.e. ratio = sqrt(ARE)), 2 decimals, J panels x S rows x N columns.
Missing cells are rendered as '--'.
"""
from pathlib import Path
import numpy as np

SCRIPT_DIR = Path(__file__).parent
PROJ_ROOT = SCRIPT_DIR.parent.parent
OUT = PROJ_ROOT / "slides" / "artifacts" / "tables"

S_MODES = [("one", r"$S{=}1$"),
            ("sqrt_n", r"$S{=}\sqrt{N}$"),
            ("match_n", r"$S{=}N$")]
J_PANELS = [(2, r"Binary choice ($M=2$)"),
             (10, r"Multinomial ($M=10$)")]
N_VALUES = [100, 500, 1000]


def se_ratio(npz_path):
    d = dict(np.load(npz_path))
    if len(d["beta_mle"]) < 2:
        return None
    var_mle = np.var(d["beta_mle"], axis=0, ddof=1).mean()
    var_cb = np.var(d["beta_cb"], axis=0, ddof=1).mean()
    return np.sqrt(var_cb / var_mle) if var_mle > 0 else None


def cell(model, s_mode, J, N):
    p = (SCRIPT_DIR / "results" / model / "raw" / s_mode
         / f"{model}_J{J}_N{N}.npz")
    if not p.exists():
        return "--"
    v = se_ratio(p)
    return f"{v:.2f}" if v is not None else "--"


def has_any(model, s_mode):
    return any(
        (SCRIPT_DIR / "results" / model / "raw" / s_mode
         / f"{model}_J{J}_N{N}.npz").exists()
        for J, _ in J_PANELS for N in N_VALUES
    )


def build_table(model):
    # Only include S modes that have at least one cell for this model.
    active_s = [(sm, lbl) for sm, lbl in S_MODES if has_any(model, sm)]

    lines = [
        f"% Auto-generated by numerical_experiments/unit_demand/sync_to_slides.py",
        f"% model: {model}, metric: s.e. ratio = sqrt(Var_CB / Var_MLE)",
        r"\begin{tabular}{l rrr}",
        r"\toprule",
        " & " + " & ".join(f"$N={N}$" for N in N_VALUES) + r" \\",
        r"\midrule",
    ]
    for i, (J, panel_label) in enumerate(J_PANELS):
        lines.append(rf"\multicolumn{{4}}{{l}}{{\textit{{{panel_label}}}}} \\[2pt]")
        for sm, sm_label in active_s:
            row = [cell(model, sm, J, N) for N in N_VALUES]
            lines.append(f"  {sm_label} & " + " & ".join(row) + r" \\")
        if i < len(J_PANELS) - 1:
            lines[-1] += "[4pt]"
    lines += [r"\bottomrule", r"\end{tabular}"]
    return "\n".join(lines) + "\n"


def _stats(npz_path, beta_star):
    d = dict(np.load(npz_path))
    bm, bc = d["beta_mle"], d["beta_cb"]
    bias_mle = np.linalg.norm(np.mean(bm - beta_star, axis=0))
    bias_cb = np.linalg.norm(np.mean(bc - beta_star, axis=0))
    rmse_mle = np.sqrt(np.mean((bm - beta_star)**2))
    rmse_cb = np.sqrt(np.mean((bc - beta_star)**2))
    return bias_mle, bias_cb, rmse_mle, rmse_cb


DISPLAY_NAME = {
    "probit": r"probit",
    "logit": r"logit",
    "probit_corr": r"probit-corr",
}


def build_misspec_table():
    """3x3 misspecification matrix: DGP (rows) x assumed estimator (cols).
    Each off-diagonal cell shows "||bias||_MLE / ||bias||_CB"."""
    raw = SCRIPT_DIR / "results" / "misspec" / "raw"
    models = ["probit", "logit", "probit_corr"]

    # Check we have data
    any_exists = any(
        (raw / f"dgp_{d}_est_{e}.npz").exists()
        for d in models for e in models if d != e
    )
    if not any_exists:
        return None

    # Grab N, J, beta from any available cell
    first = next(raw / f"dgp_{d}_est_{e}.npz" for d in models for e in models
                 if d != e and (raw / f"dgp_{d}_est_{e}.npz").exists())
    meta = dict(np.load(first))
    N, J = int(meta["N"]), int(meta["J"])
    beta = meta["beta_star"]

    def cell(dgp, est):
        if dgp == est:
            return "---"
        p = raw / f"dgp_{dgp}_est_{est}.npz"
        if not p.exists():
            return "--"
        bm, bc, _, _ = _stats(p, beta)
        return f"{bm:.2f} / {bc:.2f}"

    header = " & " + " & ".join(
        rf"\textit{{est {DISPLAY_NAME[e]}}}" for e in models) + r" \\"

    lines = [
        r"% Auto-generated by numerical_experiments/unit_demand/sync_to_slides.py",
        rf"% 3x3 misspec matrix at N={N}, J={J}. Cell = ||bias||_MLE / ||bias||_CB.",
        r"\begin{tabular}{l ccc}",
        r"\toprule",
        header,
        r"\midrule",
    ]
    for dgp in models:
        row = rf"\textit{{DGP {DISPLAY_NAME[dgp]}}} & " + " & ".join(
            cell(dgp, e) for e in models) + r" \\"
        lines.append(row)
    lines += [r"\bottomrule", r"\end{tabular}"]
    return "\n".join(lines) + "\n"


def main():
    OUT.mkdir(parents=True, exist_ok=True)
    for model in ("probit", "logit"):
        target = OUT / f"tab_unit_demand_{model}.tex"
        target.write_text(build_table(model))
        print(f"wrote {target.relative_to(PROJ_ROOT)}")

    misspec = build_misspec_table()
    if misspec is not None:
        target = OUT / "tab_unit_demand_misspec.tex"
        target.write_text(misspec)
        print(f"wrote {target.relative_to(PROJ_ROOT)}")


if __name__ == "__main__":
    main()
