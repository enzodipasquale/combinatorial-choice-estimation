#!/usr/bin/env python3
"""Write slide-ready results to ../../slides/artifacts/.

Primary output: Monte Carlo table with mean / bias / MC std / RMSE across
seeds. This is the evidence used in the paper's numerical experiments
section.

Secondary output (optional): single-seed bootstrap CIs, written only if
`results/<subdir>/bootstrap_result.json` exists. Useful for demonstrating
inference on one representative sample alongside the MC consistency
evidence.

Emits:
  slides/artifacts/tables/tab_multi_stage_production_MC.tex
      12-param Monte Carlo results (primary).
  slides/artifacts/tables/tab_multi_stage_production_boot.tex
      12-param bootstrap CIs (written only if a bootstrap JSON exists).
  slides/artifacts/figures/fig_production_networks.png
      4-panel firm-network plot.
  slides/artifacts/figures/fig_production_single.png
      Single-firm clean network plot (used on the slides).

Usage:
    python sync_to_slides.py                 # auto-detect latest MC + any bootstrap
    python sync_to_slides.py --mc-json path  # explicit MC JSON
    python sync_to_slides.py --boot-subdir boot_config_S1  # explicit bootstrap subdir
"""
import argparse
import json
import shutil
from pathlib import Path

SCRIPT_DIR = Path(__file__).resolve().parent
PROJ_ROOT = SCRIPT_DIR.parents[2]
ARTIFACTS = PROJ_ROOT / "slides" / "artifacts"

# Display order and LaTeX labels for the 12 parameters.
# Notation follows the slide frame "Multi-stage production: setup":
#   theta^{fc,k}   = stage-k fixed cost (delta_k)
#   theta^{dist,k} = stage-k HQ distance (rho_HQ_k, slide's "d_{i,ell}")
#   theta^{xi,k}   = stage-k quality loading (rho_xi_k)
#   theta^{FE,k}_r = stage-k region-r fixed effect
#   theta^{path,k} = stage-k routing distance (rho_d_k, not in slide equation)
PARAM_LABELS = [
    ("delta_1",  r"$\theta^{\text{fc},1}$"),
    ("delta_2",  r"$\theta^{\text{fc},2}$"),
    ("rho_xi_1", r"$\theta^{\xi,1}$"),
    ("rho_xi_2", r"$\theta^{\xi,2}$"),
    ("rho_HQ_1", r"$\theta^{\text{dist},1}$"),
    ("rho_HQ_2", r"$\theta^{\text{dist},2}$"),
    ("FE_1_r1",  r"$\theta^{\text{FE},1}_{1}$"),
    ("FE_1_r2",  r"$\theta^{\text{FE},1}_{2}$"),
    ("FE_2_r1",  r"$\theta^{\text{FE},2}_{1}$"),
    ("FE_2_r2",  r"$\theta^{\text{FE},2}_{2}$"),
    ("rho_d_1",  r"$\theta^{\text{path},1}$"),
    ("rho_d_2",  r"$\theta^{\text{path},2}$"),
]


def _load_json(path: Path):
    if not path.exists():
        return None
    with open(path) as f:
        return json.load(f)


def build_mc_table(mc):
    """Monte Carlo results: True | Mean | Bias | MC std | RMSE."""
    names = mc["param_names"]
    theta_true = mc["theta_true"]
    mean_hat = mc["mean_hat"]
    bias = mc["bias"]
    std_hat = mc["std_hat"]
    rmse = mc["rmse"]
    n_conv = mc.get("n_converged", mc.get("K_completed", "?"))
    K = mc.get("K_total", n_conv)
    N = mc.get("n_firms", "?")
    S = mc.get("n_simulations", "?")
    name_to_idx = {n: i for i, n in enumerate(names)}

    lines = [
        r"% Auto-generated by sync_to_slides.py (MC)",
        rf"% N={N} firms, S={S} simulations, K={n_conv}/{K} converged seeds",
        r"\renewcommand{\arraystretch}{1.1}",
        r"\begin{tabular}{@{}l rrrrr@{}}",
        r"\toprule",
        r" & True & Mean & Bias & MC s.e. & RMSE \\",
        r"\midrule",
    ]
    for key, label in PARAM_LABELS:
        if key not in name_to_idx:
            continue
        j = name_to_idx[key]
        tv = theta_true[key] if key in theta_true else float("nan")
        lines.append(
            f"  {label} & {tv:.2f} & {mean_hat[j]:.2f} & "
            f"{bias[j]:+.3f} & {std_hat[j]:.3f} & {rmse[j]:.3f} \\\\")
    lines += [
        r"\midrule",
        rf"\multicolumn{{6}}{{l}}{{\footnotesize $N{{=}}{N}$ firms, "
        rf"$S{{=}}{S}$, {n_conv}/{K} Monte Carlo seeds converged.}} \\",
        r"\bottomrule",
        r"\end{tabular}",
    ]
    return "\n".join(lines) + "\n"


def build_bootstrap_table(boot):
    """Bootstrap CIs: True | Est | s.e. | 95% CI (single representative seed)."""
    names = boot["param_names"]
    theta_true = boot["theta_true"]
    theta_hat = boot["theta_hat"]
    se = boot["se"]
    ci_lo = boot["ci_lower"]
    ci_hi = boot["ci_upper"]
    converged = boot.get("converged", [])
    n_conv = int(sum(converged)) if converged else len(theta_hat)
    B = len(boot.get("bootstrap_thetas", []))
    cfg = boot.get("config", {})
    S = cfg.get("estimation", {}).get("n_simulations", "?")
    N = cfg.get("dgp", {}).get("n_firms", "?")
    name_to_idx = {n: i for i, n in enumerate(names)}

    lines = [
        r"% Auto-generated by sync_to_slides.py (bootstrap)",
        rf"% N={N}, S={S}, B={B} Bayesian bootstrap samples ({n_conv} converged)",
        r"\renewcommand{\arraystretch}{1.1}",
        r"\begin{tabular}{@{}l rrrr@{}}",
        r"\toprule",
        r" & True & Est. & s.e. & 95\% CI \\",
        r"\midrule",
    ]
    for key, label in PARAM_LABELS:
        if key not in name_to_idx:
            continue
        j = name_to_idx[key]
        tv = theta_true[key] if key in theta_true else float("nan")
        lines.append(
            f"  {label} & {tv:.2f} & {theta_hat[j]:.2f} & "
            f"{se[j]:.3f} & [{ci_lo[j]:.2f},\\,{ci_hi[j]:.2f}] \\\\")
    lines += [
        r"\midrule",
        rf"\multicolumn{{5}}{{l}}{{\footnotesize $N{{=}}{N}$, $S{{=}}{S}$, "
        rf"$B{{=}}{B}$ ({n_conv} converged). Bayesian bootstrap.}} \\",
        r"\bottomrule",
        r"\end{tabular}",
    ]
    return "\n".join(lines) + "\n"


def pick_best_mc(mc_dir: Path):
    """Return (mc_dict, path) of the MC file with the most converged seeds."""
    best, best_path, best_n = None, None, -1
    for p in sorted(mc_dir.glob("mc_K*.json")):
        m = _load_json(p)
        if m is None or "mean_hat" not in m:
            continue
        n = m.get("n_converged", m.get("K_completed", 0))
        if n > best_n:
            best, best_path, best_n = m, p, n
    return best, best_path


def copy_figures(seed=42):
    """Copy plots/*.png -> slides/artifacts/figures/."""
    figs_dir = ARTIFACTS / "figures"
    figs_dir.mkdir(parents=True, exist_ok=True)

    mapping = [
        (SCRIPT_DIR / "plots" / f"networks_seed{seed}.png",
         figs_dir / "fig_production_networks.png"),
    ]
    singles = sorted((SCRIPT_DIR / "plots").glob(
        f"network_single_seed{seed}_firm*.png"))
    if singles:
        mapping.append((singles[0], figs_dir / "fig_production_single.png"))

    written = []
    for src, dst in mapping:
        if not src.exists():
            print(f"  skip figure: {src.relative_to(PROJ_ROOT)} not found")
            continue
        shutil.copyfile(src, dst)
        written.append(dst)
    return written


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--seed", type=int, default=42,
                        help="Seed used for the network plot (default: 42)")
    parser.add_argument("--mc-json", default=None,
                        help="Path to a specific MC JSON (default: auto-detect "
                             "best in results/monte_carlo/)")
    parser.add_argument("--boot-subdir", default=None,
                        help="Results subdir with bootstrap_result.json to "
                             "emit a supplementary CI table (default: none)")
    args = parser.parse_args()

    tables_dir = ARTIFACTS / "tables"
    tables_dir.mkdir(parents=True, exist_ok=True)

    # --- Primary: Monte Carlo table ---
    if args.mc_json:
        mc = _load_json(Path(args.mc_json))
        mc_path = Path(args.mc_json)
    else:
        mc_dir = SCRIPT_DIR / "results" / "monte_carlo"
        mc, mc_path = pick_best_mc(mc_dir) if mc_dir.exists() else (None, None)

    if mc is not None:
        out = tables_dir / "tab_multi_stage_production_MC.tex"
        out.write_text(build_mc_table(mc))
        print(f"wrote {out.relative_to(PROJ_ROOT)} "
              f"(K={mc.get('K_completed')}/{mc.get('K_total')} from "
              f"{mc_path.name})")
    else:
        print("  skip MC table: no Monte Carlo result JSON found")

    # --- Optional: bootstrap CIs (supplementary) ---
    if args.boot_subdir:
        boot_path = SCRIPT_DIR / "results" / args.boot_subdir / "bootstrap_result.json"
        boot = _load_json(boot_path)
        if boot is not None:
            out = tables_dir / "tab_multi_stage_production_boot.tex"
            out.write_text(build_bootstrap_table(boot))
            print(f"wrote {out.relative_to(PROJ_ROOT)}")
        else:
            print(f"  skip boot table: {boot_path.relative_to(PROJ_ROOT)} not found")

    # --- Figures ---
    for fig_path in copy_figures(seed=args.seed):
        print(f"wrote {fig_path.relative_to(PROJ_ROOT)}")


if __name__ == "__main__":
    main()
