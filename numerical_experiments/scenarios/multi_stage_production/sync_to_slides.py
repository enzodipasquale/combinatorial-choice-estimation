#!/usr/bin/env python3
"""Write slide-ready results to ../../slides/artifacts/.

Reads bootstrap_result.json from a results subdirectory and emits:

  slides/artifacts/tables/tab_multi_stage_production.tex
      12-parameter table: true, estimate, s.e., t-stat, 95% CI.

  slides/artifacts/figures/fig_production_networks.png
      Network plot from plots/networks_seed<seed>.png, if available.

Usage:
    python sync_to_slides.py                     # uses results/boot_config/
    python sync_to_slides.py boot_config         # explicit result subdir
"""
import argparse
import json
import shutil
import sys
from pathlib import Path

SCRIPT_DIR = Path(__file__).resolve().parent
PROJ_ROOT = SCRIPT_DIR.parents[2]
ARTIFACTS = PROJ_ROOT / "slides" / "artifacts"

# Display order and LaTeX labels for the 12 parameters.
# Notation follows the slide frame "Multi-stage production: setup":
#   theta^{fc,k}   = stage-k fixed cost (delta_k)
#   theta^{dist,k} = stage-k HQ distance (rho_HQ_k, slide's "d_{i,ell}")
#   theta^{xi,k}   = stage-k quality loading (rho_xi_k)
#   theta^{FE,k}_r = stage-k region-r fixed effect
#   theta^{path,k} = stage-k routing distance (rho_d_k, not in slide equation)
PARAM_LABELS = [
    ("delta_1",  r"$\theta^{\text{fc},1}$"),
    ("delta_2",  r"$\theta^{\text{fc},2}$"),
    ("rho_xi_1", r"$\theta^{\xi,1}$"),
    ("rho_xi_2", r"$\theta^{\xi,2}$"),
    ("rho_HQ_1", r"$\theta^{\text{dist},1}$"),
    ("rho_HQ_2", r"$\theta^{\text{dist},2}$"),
    ("FE_1_r1",  r"$\theta^{\text{FE},1}_{1}$"),
    ("FE_1_r2",  r"$\theta^{\text{FE},1}_{2}$"),
    ("FE_2_r1",  r"$\theta^{\text{FE},2}_{1}$"),
    ("FE_2_r2",  r"$\theta^{\text{FE},2}_{2}$"),
    ("rho_d_1",  r"$\theta^{\text{path},1}$"),
    ("rho_d_2",  r"$\theta^{\text{path},2}$"),
]


def load_result(result_dir):
    path = result_dir / "bootstrap_result.json"
    if not path.exists():
        return None
    with open(path) as f:
        return json.load(f)


def build_mc_table(mc):
    """Emit Monte Carlo results table: True | Mean | Bias | MC std | RMSE."""
    names = mc["param_names"]
    theta_true = mc["theta_true"]
    mean_hat = mc["mean_hat"]
    bias = mc["bias"]
    std_hat = mc["std_hat"]
    rmse = mc["rmse"]
    n_conv = mc.get("n_converged", mc.get("K_completed", "?"))
    K = mc.get("K_total", n_conv)
    N = mc.get("n_firms", "?")
    S = mc.get("n_simulations", "?")
    name_to_idx = {n: i for i, n in enumerate(names)}

    lines = [
        r"% Auto-generated by numerical_experiments/scenarios/multi_stage_production/sync_to_slides.py (MC)",
        rf"% N={N} firms, S={S} simulations, K={n_conv}/{K} converged seeds",
        r"\renewcommand{\arraystretch}{1.1}",
        r"\begin{tabular}{@{}l rrrrr@{}}",
        r"\toprule",
        r" & True & Mean & Bias & MC s.e. & RMSE \\",
        r"\midrule",
    ]
    for key, label in PARAM_LABELS:
        if key not in name_to_idx:
            continue
        j = name_to_idx[key]
        tv = theta_true[key] if key in theta_true else float("nan")
        lines.append(
            f"  {label} & {tv:.2f} & {mean_hat[j]:.2f} & "
            f"{bias[j]:+.3f} & {std_hat[j]:.3f} & {rmse[j]:.3f} \\\\")
    lines += [
        r"\midrule",
        rf"\multicolumn{{6}}{{l}}{{\footnotesize $N{{=}}{N}$ firms, $S{{=}}{S}$, "
        rf"{n_conv}/{K} Monte Carlo seeds converged.}} \\",
        r"\bottomrule",
        r"\end{tabular}",
    ]
    return "\n".join(lines) + "\n"


def load_mc(mc_path: Path):
    if not mc_path.exists():
        return None
    with open(mc_path) as f:
        return json.load(f)


def build_table(result):
    """Emit the 12-parameter bootstrap results table."""
    names = result["param_names"]
    theta_true = result["theta_true"]
    theta_hat = result["theta_hat"]
    se = result["se"]
    ci_lo = result["ci_lower"]
    ci_hi = result["ci_upper"]
    t_stats = result["t_stats"]
    converged = result.get("converged", [])
    n_conv = int(sum(converged)) if converged else len(theta_hat)
    B = len(result.get("bootstrap_thetas", []))
    cfg = result.get("config", {})
    S = cfg.get("estimation", {}).get("n_simulations", "?")
    N = cfg.get("dgp", {}).get("n_firms", "?")

    name_to_idx = {n: i for i, n in enumerate(names)}

    lines = [
        r"% Auto-generated by numerical_experiments/scenarios/multi_stage_production/sync_to_slides.py",
        rf"% N={N} firms, S={S} simulations, B={B} bootstrap samples ({n_conv} converged)",
        r"\renewcommand{\arraystretch}{1.1}",
        r"\begin{tabular}{@{}l rrrr@{}}",
        r"\toprule",
        r" & True & Est. & s.e. & 95\% CI \\",
        r"\midrule",
    ]
    for key, label in PARAM_LABELS:
        if key not in name_to_idx:
            continue
        j = name_to_idx[key]
        true = theta_true[key] if key in theta_true else float("nan")
        est = theta_hat[j]
        sej = se[j]
        lo = ci_lo[j]
        hi = ci_hi[j]
        lines.append(
            f"  {label} & {true:.2f} & {est:.2f} & {sej:.3f} & "
            f"[{lo:.2f},\\,{hi:.2f}] \\\\")
    lines += [
        r"\midrule",
        rf"\multicolumn{{5}}{{l}}{{\footnotesize $N{{=}}{N}$, $S{{=}}{S}$, "
        rf"$B{{=}}{B}$ ({n_conv} converged). Bayesian bootstrap.}} \\",
        r"\bottomrule",
        r"\end{tabular}",
    ]
    return "\n".join(lines) + "\n"


def copy_figures(seed=42):
    """Copy plots/*.png -> slides/artifacts/figures/.
    Returns list of written paths."""
    figs_dir = ARTIFACTS / "figures"
    figs_dir.mkdir(parents=True, exist_ok=True)

    mapping = [
        (SCRIPT_DIR / "plots" / f"networks_seed{seed}.png",
         figs_dir / "fig_production_networks.png"),
    ]
    # Single-firm plot: any file matching network_single_seed<seed>_firm*.png
    singles = sorted((SCRIPT_DIR / "plots").glob(
        f"network_single_seed{seed}_firm*.png"))
    if singles:
        mapping.append((singles[0], figs_dir / "fig_production_single.png"))

    written = []
    for src, dst in mapping:
        if not src.exists():
            print(f"  skip: {src.relative_to(PROJ_ROOT)} not found")
            continue
        shutil.copyfile(src, dst)
        written.append(dst)
    return written


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--seed", type=int, default=42,
                        help="Seed used for the network plot (default: 42)")
    parser.add_argument("--subdir-s1", default="boot_config_S1",
                        help="Results subdir for S=1 local bootstrap "
                             "(default: boot_config_S1)")
    parser.add_argument("--subdir-s10", default="boot_config",
                        help="Results subdir for S=10 HPC bootstrap "
                             "(default: boot_config)")
    parser.add_argument("--mc-json", default=None,
                        help="Path to Monte Carlo result JSON (default: auto-detect)")
    args = parser.parse_args()

    tables_dir = ARTIFACTS / "tables"
    tables_dir.mkdir(parents=True, exist_ok=True)

    # Monte Carlo table (Bias/RMSE across seeds) ----
    # Looks for results/monte_carlo/mc_K*.json; pick the highest-K one.
    if args.mc_json is not None:
        mc_candidates = [Path(args.mc_json)]
    else:
        mc_dir = SCRIPT_DIR / "results" / "monte_carlo"
        mc_candidates = sorted(mc_dir.glob("mc_K*.json")) if mc_dir.exists() else []

    if mc_candidates:
        # Pick the MC file with the highest number of converged seeds.
        best_mc, best_path, best_conv = None, None, -1
        for p in mc_candidates:
            m = load_mc(p)
            if m is None or "mean_hat" not in m:
                continue
            n = m.get("n_converged", m.get("K_completed", 0))
            if n > best_conv:
                best_mc, best_path, best_conv = m, p, n
        if best_mc is not None:
            out = tables_dir / "tab_multi_stage_production_MC.tex"
            out.write_text(build_mc_table(best_mc))
            print(f"wrote {out.relative_to(PROJ_ROOT)} "
                  f"(K={best_mc.get('K_completed')}/{best_mc.get('K_total')} from "
                  f"{best_path.name})")

    # Emit one table per result subdir that exists. The slide uses:
    #   \input{artifacts/tables/tab_multi_stage_production_S1}   (first beat)
    #   \input{artifacts/tables/tab_multi_stage_production_S10}  (second beat)
    for tag, subdir in [("S1", args.subdir_s1), ("S10", args.subdir_s10)]:
        result_dir = SCRIPT_DIR / "results" / subdir
        result = load_result(result_dir)
        tab_path = tables_dir / f"tab_multi_stage_production_{tag}.tex"
        if result is None:
            print(f"  skip table: {result_dir.relative_to(PROJ_ROOT)}"
                  f"/bootstrap_result.json not found")
            continue
        tab_path.write_text(build_table(result))
        print(f"wrote {tab_path.relative_to(PROJ_ROOT)}")

    for fig_path in copy_figures(seed=args.seed):
        print(f"wrote {fig_path.relative_to(PROJ_ROOT)}")


if __name__ == "__main__":
    main()
