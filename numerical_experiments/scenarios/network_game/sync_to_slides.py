#!/usr/bin/env python3
"""Write network_game MC results to slides/artifacts/.

Produces:
  * tables/tab_network_game_mc.tex    — combest MC table (Table 1-style)
  * tables/tab_network_game_paper.tex — paper's published Table 1 Panel B
  * tables/network_game_runtimes.tex  — \newcommand macros (T, S, timings)
"""

from __future__ import annotations

import json
from pathlib import Path

import numpy as np

SCRIPT_DIR = Path(__file__).parent
PROJ_ROOT = SCRIPT_DIR.parent.parent.parent
SLIDES = PROJ_ROOT / "slides" / "artifacts"
TABLES = SLIDES / "tables"
FIGURES = SLIDES / "figures"


def fmt(x, d=4):
    return f"{x:.{d}f}"


def fmt_signed(x, d=4):
    return f"{x:+.{d}f}"


def load_combest_mc():
    path = SCRIPT_DIR / "results" / "monte_carlo.json"
    if not path.exists():
        return None
    with path.open() as f:
        return json.load(f)


def load_paper_sml_mc():
    path = SCRIPT_DIR / "results" / "paper_sml_mc.json"
    if not path.exists():
        return None
    with path.open() as f:
        return json.load(f)


# ---------------------------------------------------------------------------
# Combest MC: 5 rows (4 β + δ), columns True/Mean/Bias/Std
# ---------------------------------------------------------------------------

PARAM_LABELS = [
    (0, r"$\beta_1$"),
    (1, r"$\beta_2$"),
    (2, r"$\beta_3$"),
    (3, r"$\beta_4$"),
    (4, r"$\delta$"),
]


def build_combest_table(mc):
    stats = mc["statistics"]
    theta_true = mc["theta_true"]
    means = stats["mean"]
    bias = stats["bias"]
    std = stats["std"]

    lines = [
        "% Auto-generated by numerical_experiments/scenarios/network_game/sync_to_slides.py",
        "% Combest (supermodular min-cut) Monte Carlo, max-NE DGP.",
        r"\begin{tabular}{@{}l rrrr@{}}",
        r"\toprule",
        r" & True & Mean & Bias & Std \\",
        r"\midrule",
    ]
    for idx, label in PARAM_LABELS:
        lines.append(
            f"{label} & {fmt(theta_true[idx])} & {fmt(means[idx])} "
            f"& {fmt_signed(bias[idx])} & {fmt(std[idx])} " + r"\\")
    lines += [r"\bottomrule", r"\end{tabular}"]
    return "\n".join(lines) + "\n"


# ---------------------------------------------------------------------------
# Paper's published Table 1 Panel B (for the slide comparison, hard-coded)
# ---------------------------------------------------------------------------

PAPER_PANEL_B = {
    "T": 500, "N": 1,
    "S_list": [1, 10, 100],
    "mean_delta": [0.206, 0.199, 0.197],
    "std_delta":  [0.030, 0.043, 0.051],
    "LR_size":    [0.060, 0.040, 0.072],
    "CI_cov":     [0.958, 0.896, 0.872],
    "delta_true": 0.20,
}


def build_paper_table():
    p = PAPER_PANEL_B
    lines = [
        "% Source: Graham & Gonzalez (2023) Table 1, Panel B (N=1, T=500).",
        r"\begin{tabular}{@{}l rrr@{}}",
        r"\toprule",
        rf" & $S=1$ & $S=10$ & $S=100$ \\",
        r"\midrule",
        f"mean $\\hat\\delta$ & {p['mean_delta'][0]:.3f} & "
        f"{p['mean_delta'][1]:.3f} & {p['mean_delta'][2]:.3f} " + r"\\",
        f"std $\\hat\\delta$ & {p['std_delta'][0]:.3f} & "
        f"{p['std_delta'][1]:.3f} & {p['std_delta'][2]:.3f} " + r"\\",
        f"LR test size & {p['LR_size'][0]:.3f} & "
        f"{p['LR_size'][1]:.3f} & {p['LR_size'][2]:.3f} " + r"\\",
        f"95\\% CI cov & {p['CI_cov'][0]:.3f} & "
        f"{p['CI_cov'][1]:.3f} & {p['CI_cov'][2]:.3f} " + r"\\",
        r"\bottomrule",
        r"\end{tabular}",
    ]
    return "\n".join(lines) + "\n"


# ---------------------------------------------------------------------------
# Runtime macros
# ---------------------------------------------------------------------------

def build_runtime_macros(mc):
    cfg = mc["config"]
    total = mc.get("runtime_total_s", 0.0)
    per_rep = mc.get("runtime_per_rep_s", 0.0)
    n_conv = mc.get("n_reps_converged", 0)
    T = cfg["dgp"]["T"]
    S = cfg["estimation"]["n_simulations"]
    lines = [
        "% Auto-generated network_game runtimes and dims",
        rf"\newcommand{{\networkGameT}}{{{T}}}",
        rf"\newcommand{{\networkGameS}}{{{S}}}",
        rf"\newcommand{{\networkGameNReps}}{{{n_conv}}}",
        rf"\newcommand{{\networkGameDeltaTrue}}{{{cfg['dgp']['delta_true']:.2f}}}",
        rf"\newcommand{{\networkGamePerRep}}{{{per_rep:.1f}}}",
        rf"\newcommand{{\networkGameTotalMin}}{{{total/60:.0f}}}",
    ]
    return "\n".join(lines) + "\n"


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main():
    TABLES.mkdir(parents=True, exist_ok=True)

    mc = load_combest_mc()
    if mc is None:
        print("WARN: results/monte_carlo.json not found — skipping combest tables")
    else:
        (TABLES / "tab_network_game_mc.tex").write_text(build_combest_table(mc))
        (TABLES / "network_game_runtimes.tex").write_text(
            build_runtime_macros(mc))
        print(f"wrote {TABLES / 'tab_network_game_mc.tex'}")
        print(f"wrote {TABLES / 'network_game_runtimes.tex'}")

    (TABLES / "tab_network_game_paper.tex").write_text(build_paper_table())
    print(f"wrote {TABLES / 'tab_network_game_paper.tex'}")


if __name__ == "__main__":
    main()
