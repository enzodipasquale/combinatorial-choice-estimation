#!/bin/env python
"""Distributed bootstrap estimation for quadratic knapsack / auction scenario.

Loads the DGP generated by run.py (data/ artifacts), sets up the combest Model
with QuadraticKnapsackGRB solver, and runs point estimation + distributed
bootstrap for standard errors.

theta layout: [alpha, delta_1, ..., delta_M, lambda]  (K = M + 2)

Data fed to combest:
  id_data['modular']:    (N, M, 1)    agent-item features x_ij      -> alpha
  item_data['modular']:  (M, M)       -I_M                          -> delta
  item_data['quadratic']:(M, M, 1)    0.5 * Q                       -> lambda
  id_data['capacity']:   (N,)         knapsack capacities
  item_data['weight']:   (M,)         item weights
"""

import json
import sys
import argparse
from pathlib import Path
import numpy as np
import yaml
from scipy import sparse
import combest as ce
from combest.utils import get_logger
from combest.estimation.callbacks import adaptive_gurobi_timeout

logger = get_logger(__name__)

BASE = Path(__file__).resolve().parent


def load_dgp_artifacts(data_dir):
    """Load the DGP artifacts saved by run.py."""
    return {
        'obs_bundles': np.load(data_dir / 'obs_bundles.npy'),
        'x_modular': np.load(data_dir / 'x_modular.npy'),
        'weights': np.load(data_dir / 'weights.npy'),
        'capacities': np.load(data_dir / 'capacities.npy'),
        'delta_star': np.load(data_dir / 'delta_star.npy'),
        'beta_star': np.load(data_dir / 'beta_star.npy'),
        'Q_sparse': sparse.load_npz(data_dir / 'Q_sparse.npz'),
    }


def load_theta_star(result_path):
    """Load theta_star from result.json."""
    with open(result_path) as f:
        result = json.load(f)
    alpha = result['theta_star']['alpha']
    lambda_ = result['theta_star']['lambda']
    delta = np.load(BASE / 'data' / 'delta_star.npy')
    return np.concatenate([[alpha], delta, [lambda_]])


def main(boot_config_path):
    with open(boot_config_path) as f:
        boot_cfg = yaml.safe_load(f)

    with open(BASE / 'config.yaml') as f:
        main_cfg = yaml.safe_load(f)

    size_name = boot_cfg['size']
    size_cfg = main_cfg['sizes'][size_name]
    N = size_cfg['N']
    M = size_cfg['M']
    K = M + 2

    est_cfg = boot_cfg['estimation']
    bstrap = boot_cfg['bootstrap']
    callbacks = boot_cfg['callbacks']
    sigma = main_cfg['dgp']['sigma']

    results_dir = BASE / 'results' / Path(boot_config_path).stem
    results_dir.mkdir(parents=True, exist_ok=True)

    model = ce.Model()

    # ---- Load DGP artifacts (root only) ----

    if model.is_root():
        dgp = load_dgp_artifacts(BASE / 'data')
        theta_star = load_theta_star(BASE / 'results' / 'result.json')

        obs_bundles = dgp['obs_bundles']       # (N, M) bool
        x = dgp['x_modular']                   # (N, M)

        # Verify data matches the requested size
        assert obs_bundles.shape == (N, M), \
            f"obs_bundles shape {obs_bundles.shape} != ({N}, {M}). " \
            f"Re-run: python run.py --size {size_name}"
        assert len(theta_star) == K, \
            f"theta_star length {len(theta_star)} != K={K}"
        weights = dgp['weights']               # (M,)
        capacities = dgp['capacities']         # (N,)
        Q_dense = dgp['Q_sparse'].toarray()    # (M, M)

        # Build data arrays for combest's QuadraticObjectiveMixin.
        # id_data['modular']: (N, M, 1) — agent-item feature for alpha
        modular_agent = x[:, :, None]

        # item_data['modular']: (M, M) — negative identity for delta
        # delta enters as -sum_j delta_j b_j, so feature = -I_M
        modular_item = -np.eye(M)

        # item_data['quadratic']: (M, M, 1) — adjacency for lambda
        # Gurobi computes b^T Q b = 2 sum_{j<j'}, so store 0.5 * Q.
        quadratic_item = (0.5 * Q_dense)[:, :, None]

        input_data = {
            'id_data': {
                'obs_bundles': obs_bundles,
                'modular': modular_agent,
                'capacity': capacities,
            },
            'item_data': {
                'modular': modular_item,
                'quadratic': quadratic_item,
                'weight': weights,
            },
        }

        logger.info(f"DGP loaded: N={N}, M={M}, K={K}")
        logger.info(f"theta_star: alpha={theta_star[0]:.4f}, "
                    f"delta std={theta_star[1:M+1].std():.3f}, "
                    f"lambda={theta_star[-1]:.4f}")
    else:
        input_data = None
        theta_star = None

    # ---- Configure combest model ----

    # theta bounds: alpha >= 0, delta unconstrained, lambda >= 0
    lbs = {0: 0, K - 1: 0}
    theta_bounds = {'lb': -20, 'ub': 20, 'lbs': lbs}

    model_cfg = {
        'dimensions': {
            'n_obs': N,
            'n_items': M,
            'n_covariates': K,
            'n_simulations': est_cfg['n_simulations'],
        },
        'subproblem': {
            'gurobi_params': {
                'MIPGap': main_cfg['solver']['MIPGap'],
                'TimeLimit': size_cfg['gurobi_timeout'],
                'OutputFlag': 0,
            },
        },
        'row_generation': {
            'max_iters': est_cfg['max_iters'],
            'tolerance': est_cfg['tolerance'],
            'theta_bounds': theta_bounds,
        },
        'standard_errors': {
            'rowgen_tol': bstrap.get('rowgen_tol', 1e-3),
            'rowgen_max_iters': bstrap.get('rowgen_max_iters', 250),
        },
    }
    model.load_config(model_cfg)
    model.data.load_and_distribute_input_data(input_data)

    # ---- Error oracle: i.i.d. modular errors ----

    model.features.build_local_modular_error_oracle(
        seed=main_cfg['seeds']['error'], sigma=sigma)

    # ---- Covariates oracle: built from quadratic data ----

    model.features.build_quadratic_covariates_from_data()

    # ---- Load solver ----

    from combest.subproblems.registry.quadratic_obj.quadratic_knapsack import (
        QuadraticKnapsackGRBSolver,
    )
    model.subproblems.load_solver(QuadraticKnapsackGRBSolver)
    model.subproblems.initialize_solver()

    # ---- Distributed bootstrap ----

    pt_cb, _ = adaptive_gurobi_timeout(callbacks['row_gen'])
    _, dist_cb = adaptive_gurobi_timeout(callbacks['boot'])

    def boot_callback(it, boot, master):
        dist_cb(it, boot, master)
        strip = callbacks.get('boot_strip')
        if master is not None and it == 0 and strip:
            master.strip_slack_constraints(
                percentile=strip['percentile'],
                hard_threshold=strip['hard_threshold'])

    if model.is_root():
        logger.info("")
        logger.info("=" * 60)
        logger.info(f"  Distributed bootstrap (K={K}, S={est_cfg['n_simulations']}, "
                    f"B={bstrap['num_samples']})")
        logger.info("=" * 60)

    se = model.standard_errors.compute_distributed_bootstrap(
        num_bootstrap=bstrap['num_samples'],
        seed=bstrap['seed'],
        verbose=True,
        pt_estimate_callbacks=(None, pt_cb),
        bootstrap_callback=boot_callback,
        method='bayesian',
        save_model_dir=str(results_dir / 'checkpoints'),
        load_model_dir=str(results_dir / 'checkpoints'),
    )

    # ---- Save results ----

    if model.is_root() and se is not None:
        param_names = ['alpha'] + [f'delta_{j+1}' for j in range(M)] + ['lambda']

        logger.info("")
        logger.info("=" * 60)
        logger.info(f"  Bootstrap results (N={N}, M={M}, K={K})")
        logger.info("=" * 60)
        logger.info(f"  {'Param':<12} {'True':>8} {'Mean':>8} {'SE':>8} "
                    f"{'t-stat':>8}")
        logger.info(f"  {'-'*44}")
        # Print alpha, a few deltas, and lambda
        show = [0] + list(range(1, min(6, M + 1))) + [K - 1]
        for j in show:
            logger.info(f"  {param_names[j]:<12} {theta_star[j]:>8.4f} "
                        f"{se.mean[j]:>8.4f} {se.se[j]:>8.4f} "
                        f"{se.t_stats[j]:>8.2f}")
        if M > 5:
            logger.info(f"  ... ({M - 5} delta parameters omitted)")

        n_conv = int(se.converged.sum()) if se.converged is not None else '?'
        logger.info(f"\n  Converged samples: {n_conv} / {bstrap['num_samples']}")

        out = {
            'scenario': 'quadratic_knapsack',
            'size': {'N': N, 'M': M, 'K': K},
            'theta_true': theta_star.tolist(),
            'theta_hat': se.mean.tolist(),
            'se': se.se.tolist(),
            't_stats': se.t_stats.tolist(),
            'ci_lower': se.ci_lower.tolist(),
            'ci_upper': se.ci_upper.tolist(),
            'bootstrap_thetas': se.samples.tolist(),
            'converged': se.converged.tolist() if se.converged is not None else None,
            'param_names': param_names,
            'config': boot_cfg,
        }
        if se.u_samples is not None:
            out['bootstrap_u_hat'] = se.u_samples.tolist()

        out_path = results_dir / 'bootstrap_result.json'
        with open(out_path, 'w') as f:
            json.dump(out, f, indent=2)
        logger.info(f"  Saved -> {out_path}")


if __name__ == '__main__':
    parser = argparse.ArgumentParser(
        description='Distributed bootstrap for quadratic knapsack scenario')
    parser.add_argument('config', nargs='?', default='boot_config.yaml',
                        help='Path to bootstrap config YAML')
    main(parser.parse_args().config)
